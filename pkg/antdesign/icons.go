// NewAntIcon 创建预置的 Ant Design 图标。
func NewAntIcon(name AntIconName) tenon.Component {
	pathData, ok := iconPaths[name]
	if !ok {
		return nil
	}
	return components.NewSVGIcon(pathData)
}

// NewAntIconWithColor 创建指定颜色的预置图标。
func NewAntIconWithColor(name AntIconName, clr color.Color) tenon.Component {
	icon := NewAntIcon(name)
	if icon == nil {
		return nil
	}
	if si, ok := icon.(*components.SVGIcon); ok {
		si.SetColor(clr)
	}
	return icon
}
