// PerformanceOverlay 是一个调试覆盖层，显示 FPS、帧时间、布局/绘制统计。
type PerformanceOverlay struct {
	visible         bool
	x, y            float32
	bgColor         color.Color
	textColor       color.Color
	lastUpdate      time.Time
	frameCount      int
	currentFPS      float64
	frameTime       time.Duration
	layoutCount     int
	paintCount      int
	buildCount      int
}

// NewPerformanceOverlay 创建性能覆盖层。
func NewPerformanceOverlay() *PerformanceOverlay {
	return &PerformanceOverlay{
		visible:   true,
		x:         10,
		y:         10,
		bgColor:   color.RGBA{A: 180},
		textColor: color.RGBA{R: 0, G: 255, B: 0, A: 255}, // 绿色
		lastUpdate: time.Now(),
	}
}

// SetVisible 设置是否可见。
func (p *PerformanceOverlay) SetVisible(v bool) {
	p.visible = v
}

// IsVisible 返回是否可见。
func (p *PerformanceOverlay) IsVisible() bool {
	return p.visible
}

// Toggle 切换可见性。
func (p *PerformanceOverlay) Toggle() {
	p.visible = !p.visible
}

// SetPosition 设置覆盖层位置。
func (p *PerformanceOverlay) SetPosition(x, y float32) {
	p.x = x
	p.y = y
}

// RecordFrame 记录一帧数据，应在 Engine.Draw 中调用。
func (p *PerformanceOverlay) RecordFrame(buildTime, layoutTime, paintTime time.Duration) {
	p.frameCount++
	p.buildCount++
	p.layoutCount++
	p.paintCount++

	now := time.Now()
	elapsed := now.Sub(p.lastUpdate)
	if elapsed >= time.Second {
		p.currentFPS = float64(p.frameCount) / elapsed.Seconds()
		p.frameTime = elapsed / time.Duration(p.frameCount)
		p.frameCount = 0
		p.lastUpdate = now
	}
}

// Paint 绘制覆盖层到屏幕上。
// 极简横向单行：FPS + 帧时间，占据左上角极小空间，不遮挡界面。
func (p *PerformanceOverlay) Paint(screen *ebiten.Image, offset render.Offset) {
	if !p.visible {
		return
	}

	// 只显示核心信息，横向单行
	line := fmt.Sprintf("%.0f FPS %s", p.currentFPS, p.frameTime.Round(time.Microsecond))
	// 5x7 像素字体，每个字符占 6px 宽，高度 7px
	textW := float32(len(line) * 6)
	textH := float32(9)
	pad := float32(4)
	bgW := textW + pad*2
	bgH := textH + pad*2

	// 极小的半透明背景条（左上角）
	drawRect(screen, p.x+offset.X, p.y+offset.Y, bgW, bgH, p.bgColor)

	// 单行横向文本
	drawDebugText(screen, line, p.x+offset.X+pad, p.y+offset.Y+pad, p.textColor)
}

// drawRect 绘制矩形。
func drawRect(screen *ebiten.Image, x, y, w, h float32, c color.Color) {
	r, g, b, a := c.RGBA()
	cr, cg, cb, ca := uint8(r>>8), uint8(g>>8), uint8(b>>8), uint8(a>>8)
	clr := color.RGBA{R: cr, G: cg, B: cb, A: ca}

	for py := int(y); py < int(y+h); py++ {
		for px := int(x); px < int(x+w); px++ {
			if px >= 0 && py >= 0 {
				screen.Set(px, py, clr)
			}
		}
	}
}

// drawDebugText 使用简单像素字体绘制文本（仅 ASCII）。
// 实际项目中应使用 fonts 包的完整字体渲染。
func drawDebugText(screen *ebiten.Image, text string, x, y float32, c color.Color) {
	r, g, b, a := c.RGBA()
	cr, cg, cb, ca := uint8(r>>8), uint8(g>>8), uint8(b>>8), uint8(a>>8)
	clr := color.RGBA{R: cr, G: cg, B: cb, A: ca}

	cx := int(x)
	for _, ch := range text {
		if ch == ' ' {
			cx += 6
			continue
		}
		// 简单的 5x7 像素字符渲染
		pattern := getDebugCharPattern(ch)
		for row := 0; row < 7; row++ {
			for col := 0; col < 5; col++ {
				if pattern[row]&(1<<(4-col)) != 0 {
					px := cx + col
					py := int(y) + row
					if px >= 0 && py >= 0 {
						screen.Set(px, py, clr)
					}
				}
			}
		}
		cx += 6
	}
}

